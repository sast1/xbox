package com.example.version1.roles;

public class Comprador {

    String idcom;
    String comprador;
    String gmailcom;
    String cedulacom;
    String telefonocom;
    String juego;
    String costo;


    public Comprador() {
    }


    public String getIdcom() {
        return idcom;
    }

    public void setIdcom(String idcom) {
        this.idcom = idcom;
    }

    public String getComprador() {
        return comprador;
    }

    public void setComprador(String comprador) {
        this.comprador = comprador;
    }

    public String getGmailcom() {
        return gmailcom;
    }

    public void setGmailcom(String gmailcom) {
        this.gmailcom = gmailcom;
    }

    public String getCedulacom() {
        return cedulacom;
    }

    public void setCedulacom(String cedulacom) {
        this.cedulacom = cedulacom;
    }

    public String getTelefonocom() {
        return telefonocom;
    }

    public void setTelefonocom(String telefonocom) {
        this.telefonocom = telefonocom;
    }

    public String getJuego() {
        return juego;
    }

    public void setJuego(String juego) {
        this.juego = juego;
    }

    public String getCosto() {
        return costo;
    }

    public void setCosto(String costo) {
        this.costo = costo;
    }
}
